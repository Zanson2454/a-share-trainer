"""
/help — 显示所有命令列表和使用说明
"""
from ..messaging import MessageBus


def execute(args: list[str] = None) -> str:
    """执行 /help 命令"""
    text = """## 📋 A股训练系统 — 命令列表

### 日常命令
| 命令 | 说明 | 示例 |
|------|------|------|
| `/帮助` | 显示此帮助 | `/帮助` |
| `/学习` | 每日学习主题 + 教练提问 | `/学习` |
| `/盘前` | 盘前环境分析 | `/盘前` |
| `/选股` | 五维评分筛选候选股 | `/选股` |
| `/个股` | 单只股票深度分析 | `/个股 600519` |
| `/复盘` | 每日复盘模板 | `/复盘` |
| `/策略` | 策略库管理 | `/策略` 或 `/策略 均线金叉` |
| `/回测` | 策略历史回测 | `/回测 均线金叉 2024-01-01 2024-12-31` |

### ⚠️ 重要提示
- 所有输出仅用于学习研究，不构成投资建议
- 不会自动下单，不会直接给买卖结论
- 每项分析都标注依据、反证条件、风险点

### 📊 消息渠道
"""
    text += MessageBus.status()
